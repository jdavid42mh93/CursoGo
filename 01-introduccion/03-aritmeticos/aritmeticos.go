package main

import "fmt"

func main() {
	a := 20
	b := 10

	//suma
	result := a + b

	fmt.Println("Suma", result)

	//resta
	result = a - b
	fmt.Println("Resta", result)

	//multiplicacion
	result = a * b
	fmt.Println("Multiplicacion", result)

	//division
	result = a / b
	fmt.Println("Division", result)

	//division
	div := 3.0 / 2.0
	fmt.Println("Division2", div)

	//modulo
	result = a % b
	fmt.Println("Modulo", result)

}
